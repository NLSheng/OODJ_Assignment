    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        String carID = carIDField.getText().toString();
        String carBrand = carBrandField.getText().toString();
        String carModel = carModelField.getText().toString();
        String carColor = carColorField.getText().toString();
        String carYear= carYearField.getText().toString();
        String carPrice = carPriceField.getText().toString();
        String totalRentPrice = totalPriceField.getText().toString();
        Date cID = rentOutDateField.getDate();
        Date cOD = returnDateField.getDate();
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        String rentOutDate = simpleDateFormat.format(cID);
        String returnDate = simpleDateFormat.format(cOD);
        String bookingStatus = "pending";

        Car car = new Car(carID, carBrand, carModel, carColor, carYear, carPrice);
        Booking booking = new Booking(bookingStatus, rentOutDate, returnDate, totalRentPrice);
        Customer customer = new Customer(car, booking);
        customer.rentCar(this);
        
        dispose();
    }//GEN-LAST:event_bookBtnActionPerformed

