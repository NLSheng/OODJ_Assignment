    private void rentOutDateFieldPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_rentOutDateFieldPropertyChange
        try {
            Date rentOutDate = rentOutDateField.getDate();
            Date returnDate = returnDateField.getDate();
            String carPriceString = carPriceField.getText().toString();
            double carPrice = Double.parseDouble(carPriceString);
            if (rentOutDate != null) {
                returnDateField.setEnabled(rootPaneCheckingEnabled);
                returnDateField.setMinSelectableDate(rentOutDate);
            }
            if (returnDate != null) {
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH.mm");
                Date cID = rentOutDateField.getDate();
                Date cOD = returnDateField.getDate();

                long ms = Math.abs(cID.getTime() - cOD.getTime());
                long days = TimeUnit.DAYS.convert(ms, TimeUnit.MILLISECONDS);

                totalRentDayField.setText(Long.toString(days) + " day ");
                double totalRentPrice;
                totalRentPrice = days * carPrice;

                totalPriceField.setText("RM " + String.valueOf(totalRentPrice));
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_rentOutDateFieldPropertyChange

